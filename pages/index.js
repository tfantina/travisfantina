import Head from 'next/head'
import Image from 'next/image'
import Header from '../components/header'
import Intro from '../components/intro'
import Footer from '../components/footer'
import { projectBlock } from '../components/project.js'

import tinaFormOptions from '../components/tina_form_options'
import {
  useGithubJsonForm,
  useGithubToolbarPlugins
} from 'react-tinacms-github'
import { getGithubPreviewProps, parseJson } from 'next-tinacms-github'
import { GetStaticProps } from 'next'
import { InlineForm, InlineBlocks } from 'react-tinacms-inline'
import { usePreviewSrc, usePlugin, useCMS, useForm } from 'tinacms'

export default function Home({ file }) {
  const data = file.data
  const cms = useCMS()
  const [pageData, form] = useGithubJsonForm(file, tinaFormOptions)
  usePlugin(form)
  useGithubToolbarPlugins()

  return (
    <InlineForm form={form}>
      <Head>
        <title>{pageData.header.title}</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>
      <Header />

      <div className='container'>
        <Intro pageData={pageData} />
        <div className='row projects-row'>
          <h2 class='projects__title'> Projects </h2>
        </div>
        <div className='row'>
          <div className='projects'>
            <InlineBlocks name='blocks' blocks={PROJECT_BLOCKS} />
          </div>
        </div>
      </div>

      <Footer />
    </InlineForm>
  )
}

const PROJECT_BLOCKS = { project: projectBlock }

export const getStaticProps = async function ({ preview, previewData }) {
  if (preview) {
    return getGithubPreviewProps({
      ...previewData,
      fileRelativePath: 'content/home.json',
      parse: parseJson
    })
  }
  return {
    props: {
      sourceProvider: null,
      error: null,
      preview: false,
      file: {
        fileRelativePath: 'content/home.json',
        data: (await import('../content/home.json')).default
      }
    }
  }
}
